    /**
     * Convert unicode string to English
     *
     * @param  string $str The string to convert
     * @return string      The converted string
     */
    public static function convert($str)
    {
        if (empty($str) || (strlen($str) == strlen(utf8_decode($str)))) { // not UNICODE
            return $str;
        }

        return self::mapCharacters($str);
    }

    /**
     * Convert simple unicode chars to English
     *
     * @param  string $str
     * @return string
     */
    private static function mapCharacters($str)
    {
        self::setMappings();
        $str = str_replace(array_keys(self::$mappings), array_values(self::$mappings), $str);
        return str_replace('?', '', utf8_decode($str));
    }

